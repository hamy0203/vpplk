export function icon(n) {
  var s = document.createElement("span");
  s.innerHTML = IC[n] || "";
  s.style.display = "inline-flex";
  s.style.alignItems = "center";
  return s;
}

export function gid() {
  return Math.random().toString(36).substring(2, 11);
}

export function pad(v) {
  v = String(v);
  return v.length < 2 ? "0" + v : v;
}

export function thuaName(v) {
  if (!v.nong_truong && !v.lo && !v.thua) return "";
  return (v.nong_truong || "") + "/" + (v.lo || "") + "/" + (v.thua || "");
}

export function dpName(v, lan, doLai) {
  var base = v ? thuaName(v) : "";
  if (doLai && lan >= 1) base += " - " + T("lan") + " " + lan;
  return base;
}

export function el(tag, at, ch) {
  var e = document.createElement(tag);
  if (at) Object.keys(at).forEach(function(k) {
    if (k === "style" && typeof at[k] === "object") Object.assign(e.style, at[k]);
    else if (k.indexOf("on") === 0) e.addEventListener(k.substring(2).toLowerCase(), at[k]);
    else if (k === "className") e.className = at[k];
    else e.setAttribute(k, at[k]);
  });
  if (ch != null) {
    if (Array.isArray(ch)) ch.forEach(function(c) { if (c != null) e.appendChild(typeof c === "object" ? c : document.createTextNode(String(c))); });
    else e.appendChild(typeof ch === "object" ? ch : document.createTextNode(String(ch)));
  }
  return e;
}
